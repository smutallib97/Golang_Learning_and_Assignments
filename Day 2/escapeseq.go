package main

import "fmt"

func main() {
	/*
		\b - Backspace
		\f - Form Feed
		\n - New line
		\r - Carriage Return
		\t - Horizontal Tab
		\v - Vertical Tab
		\\ - Backslash
		\' - Single Quote
		\" - Double Quote
	*/

	fmt.Printf("Hello\bBridgelabzians \n")
	fmt.Printf("Hello \f Bridgelabzians\n")
	fmt.Printf("Hello \n Bridgelabzians\n")
	fmt.Printf("Hello \r Bridgelabzians\n")
	fmt.Printf("Hello \t Bridgelabzians\n")
	fmt.Printf("Hello \v Bridgelabzians\n")
	fmt.Printf("'Hello Bridgelabzians'\n")
	fmt.Printf(" \" Hello Bridgelabzians\" \n")
}
